int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb > 0)
	{
		while (i * i <= nb && i * i > 0)
		{
			if (nb / i == i && nb % i == 0)
				return (i);
			i++;
		}
		return (0);
	}
	else
		return (0);
}
