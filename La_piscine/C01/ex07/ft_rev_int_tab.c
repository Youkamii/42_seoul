#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;
	int	center;
	int	i;

	center = size / 2;
	i = 0;
	while (i < center)
	{
		temp = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = temp;
		i++;
	}
}
