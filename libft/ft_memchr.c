#include "libft.h"
#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*tmp_s;

	tmp_s = (unsigned char *)s;
	while (n)
	{
		if (*tmp_s != ((unsigned char)c))
			tmp_s++;
		else
			return (tmp_s);
		n--;
	}
	return (0);
}
